#!/usr/bin/env node
/**
 * geocode-locations.js
 * One-time setup script — run ONCE after seeding the database.
 * Reads beach_access_locations from Supabase, geocodes each address
 * via Nominatim (OpenStreetMap), then outputs SQL UPDATE statements.
 *
 * Usage:
 *   node scripts/geocode-locations.js > /tmp/geocode-updates.sql
 *   # Review the output, then paste into Supabase SQL Editor.
 *
 * Requirements:
 *   npm install @supabase/supabase-js node-fetch
 *
 * Rate limit: Nominatim requires ≤ 1 request/second.
 * This script enforces a 1.1s delay between requests.
 * ~100 locations ≈ 2 minutes to complete.
 *
 * Nominatim terms of service: https://operations.osmfoundation.org/policies/nominatim/
 * A descriptive User-Agent header is required (set below).
 */

const { createClient } = require('@supabase/supabase-js');

// TODO: Replace these with your actual Supabase URL and service role key.
// Do NOT commit the service role key to git.
const SUPABASE_URL     = process.env.SUPABASE_URL     || 'YOUR_SUPABASE_URL';
const SUPABASE_SERVICE = process.env.SUPABASE_SERVICE_ROLE_KEY || 'YOUR_SERVICE_ROLE_KEY';

const NOMINATIM_URL    = 'https://nominatim.openstreetmap.org/search';
const USER_AGENT       = 'LMU-Beach-Bonfires-Geocoder/1.0 (hello@lightmeupbonfires.com)';
const DELAY_MS         = 1100; // 1.1s between requests (Nominatim requires ≤ 1/s)

const supabase = createClient(SUPABASE_URL, SUPABASE_SERVICE);

// GEOCODING_BIAS — viewbox snaps Nominatim results to the coastline.
// If a specific pin is still slightly off after re-geocoding, manually
// correct its lat/lng directly in the beach_access_locations table in Supabase.
// That value will override the geocoded one since it's stored, not recalculated.
const PCB_BBOX  = 'viewbox=-86.4800,30.1400,-85.6500,30.2200&bounded=1';
const BEACH_30A_BBOX = 'viewbox=-86.6000,30.2600,-86.1500,30.3400&bounded=1';

const PCB_KEYWORDS = [
  'Panama City Beach', 'Gulf Dr', 'Surf Dr', 'Front Beach Rd',
  'Beach Blvd', 'Bay County'
];
const BEACH_30A_KEYWORDS = [
  '30A', 'Walton', 'Santa Rosa Beach', 'Inlet Beach', 'Seaside',
  'Rosemary Beach', 'Grayton', 'Blue Mountain', 'Dune Allen',
  'Fort Panic', 'Ed Walline', 'Destin'
];

function detectBbox(address) {
  const addr = (address || '').toLowerCase();
  if (BEACH_30A_KEYWORDS.some(kw => addr.includes(kw.toLowerCase()))) return BEACH_30A_BBOX;
  if (PCB_KEYWORDS.some(kw => addr.includes(kw.toLowerCase())))        return PCB_BBOX;
  return PCB_BBOX; // default to PCB when unclear
}

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function geocodeAddress(address) {
  const bbox    = detectBbox(address);
  const encoded = encodeURIComponent(address);
  // BEACHFRONT_GEOCODING — coastline bias applied via viewbox parameter.
  // If pins still land in water for specific addresses, manually override
  // lat/lng for that location in the beach_access_locations table.
  const url = `${NOMINATIM_URL}?format=json&q=${encoded}&limit=1&countrycodes=us&${bbox}`;

  const res = await fetch(url, {
    headers: { 'User-Agent': USER_AGENT }
  });

  if (!res.ok) {
    throw new Error(`Nominatim HTTP ${res.status} for: ${address}`);
  }

  const data = await res.json();
  if (!data || data.length === 0) {
    return null; // Address not found
  }

  return {
    lat: parseFloat(data[0].lat),
    lon: parseFloat(data[0].lon),
    display_name: data[0].display_name
  };
}

async function main() {
  console.error('Fetching beach access locations from Supabase...');

  const { data: locations, error } = await supabase
    .from('beach_access_locations')
    .select('id, name, address, latitude, longitude')
    .order('name');

  if (error) {
    console.error('Supabase error:', error.message);
    process.exit(1);
  }

  console.error(`Found ${locations.length} locations to geocode.`);
  console.error('Starting geocoding — this will take ~2 minutes...\n');

  // Output header
  console.log('-- Geocoded coordinates from Nominatim (OpenStreetMap)');
  console.log('-- Generated by scripts/geocode-locations.js');
  console.log('-- Apply these updates in Supabase SQL Editor.\n');
  console.log('BEGIN;');

  let succeeded = 0;
  let failed = 0;

  for (const loc of locations) {
    await sleep(DELAY_MS);

    try {
      const result = await geocodeAddress(loc.address);

      if (result) {
        console.log(
          `UPDATE beach_access_locations SET latitude = ${result.lat}, longitude = ${result.lon} WHERE id = '${loc.id}'; -- ${loc.name}`
        );
        console.error(`  ✓ ${loc.name}: ${result.lat}, ${result.lon}`);
        succeeded++;
      } else {
        console.log(`-- NOT FOUND: ${loc.name} (${loc.address})`);
        console.error(`  ✗ Not found: ${loc.name}`);
        failed++;
      }
    } catch (err) {
      console.log(`-- ERROR: ${loc.name} — ${err.message}`);
      console.error(`  ✗ Error: ${loc.name}: ${err.message}`);
      failed++;
    }
  }

  console.log('\nCOMMIT;');
  console.error(`\nDone. ${succeeded} succeeded, ${failed} failed.`);

  if (failed > 0) {
    console.error('For failed addresses, manually look up coordinates on:');
    console.error('https://nominatim.openstreetmap.org/');
    console.error('and add UPDATE statements for those rows.');
  }
}

main().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});
